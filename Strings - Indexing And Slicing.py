# indexing (accessing single element)

mystring = "This is a string"
print(mystring[8])# T
print(mystring[5])  # h

print(mystring[-1])  # g 
print(mystring[-5])  # s

# slicing (accessing multiple elements)
#[start:End]
# [start:End:Step]

print(mystring[0:4])  # This
print(mystring[3:5]) # is

print(mystring[0:10]) # This is a


